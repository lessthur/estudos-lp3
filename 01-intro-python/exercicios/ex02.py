# Ex02 - Escreva um programa em Python que solicite ao usuário três números
# e apresente a média aritmética dos números informados.

numero1 = float(input("Digite o 1º número: "))
numero2 = float(input("Digite o 2º número: "))
numero3 = float(input("Digite o 3º número: "))

media = (numero1 + numero2 + numero3) / 3
print("Média: ", media)

